class varSize {
    public static void main(String args[]) {
        byte a = 127;
        System.out.println(a);
        
        short b = 24343;
        System.out.println(b);
        
        int c = 1243244343;
        System.out.println(c);
        
        long d = 1626234623626422363L;
        System.out.println(d);
        
        float e = 23.3F;
        System.out.println(e);
        
        boolean f = false;
        System.out.println(f);
        
        char g = 'b';
        System.out.print(g);
    }
}