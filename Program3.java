class Program3 {
    public static void main(String args[]) {
        int a=5;
        int b=6;
        int sum=a+b;
        System.out.println("Sum = "+sum);
        int dif=a-b;
        System.out.println("Difference = "+dif);
        int pro=a*b;
        System.out.println("Product = "+pro);
        int quo=a/b;
        System.out.println("Quotient = "+quo);
        int rem=a%b;
        System.out.println("Remainder = "+rem);
    }
}